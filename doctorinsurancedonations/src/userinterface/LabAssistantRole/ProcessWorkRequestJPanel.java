    private void btnSbmtRsltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSbmtRsltActionPerformed
        String labResult = txtLbRst.getText().trim();
        if (labResult.equals("")) {
            JOptionPane.showMessageDialog(null, "Result is mandatory");
            return;
        } else {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to proceed?");
            if (dialogResult == JOptionPane.YES_OPTION) {
                patientTreatmentWorkRequest.setResultFromLab(labResult);
                patientTreatmentWorkRequest.setReqStatus("Lab Test Completed");
                JOptionPane.showMessageDialog(null, "Result submitted successfully");
                txtLbRst.setText("");
                btnSbmtRslt.setEnabled(false);
            }

        }


    }//GEN-LAST:event_btnSbmtRsltActionPerformed

